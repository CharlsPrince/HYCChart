#import "RootViewController.h"
#import "ChartViewController.h"

@interface RootViewController ()
@property (nonatomic, strong) NSArray *dataSource;
@end

static NSString *const RootCell = @"RootCell";

@implementation RootViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    self.title = @"图表Demo";
    
    self.dataSource = @[@"Chart",@"Chart"];
    
    [self.tableView registerClass:[UITableViewCell class] forCellReuseIdentifier:RootCell];
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.dataSource.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:RootCell forIndexPath:indexPath];
    cell.textLabel.text = self.dataSource[indexPath.row];
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    ChartViewController *chartController = [[ChartViewController alloc] init];
    chartController.title = @"图表";
    [self.navigationController pushViewController:chartController animated:YES];
}

